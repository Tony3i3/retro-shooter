// ─── Sprite Drawing ───────────────────────────────────────────────────────────
// All sprites are pixel arrays. 0 = transparent, 1+ = palette color index.
// Sprites are drawn facing RIGHT (angle 0). Rotation is applied before calling.

const PIXEL = 3; // each sprite pixel = 3x3 canvas pixels

function drawSprite(ctx, pixels, palette, scale) {
    scale = scale || PIXEL;
    const rows = pixels.length;
    const cols = pixels[0].length;
    const ox = -(cols * scale) / 2;
    const oy = -(rows * scale) / 2;
    for (let r = 0; r < rows; r++) {
        for (let c = 0; c < cols; c++) {
            const ci = pixels[r][c];
            if (!ci) continue;
            ctx.fillStyle = palette[ci];
            ctx.fillRect(ox + c * scale, oy + r * scale, scale, scale);
        }
    }
}

